function setup(){

    var canvas = createCanvas(window.innerWidth, window.innerHeight); // smaller then the full window.
    var x = (windowWidth - width) / 2; // finding the center coordinates
    var y = (windowHeight - height) / 2; 
    //console.log(y);

    MaxValues = round(innerWidth / 50) - 2;
    console.log(MaxValues + " MaxValues");
    tree = new Tree();
    randomArray();
    console.log(tree.height());
}

function draw(){

    background(25);

    push();
    fill(255);
    frameUpper();
    pop();

    drawTree();
}

function randomArray(){
    Array_Random = [];

    for(var i = 0; i < MaxValues; i++){
        var temp = floor(random(10, 99))
        Array_Random.push(temp);
        tree.addValues(temp);
    }

    // console.log(tree);
    tree.traverse();
    //Sorted_Array = Array_Random.sort();
}

function frameUpper(){
    rect(0, 0, innerWidth, frameHeight);
    for(var i = 0; i < MaxValues ; i++){
        push();
        fill(0);
        text(Array_Random[i], i*50+50, 25);
        pop();
    }
}

function drawTree(){
    tree.draw();
}

function drawNode(x, y){
    fill(255);  
    circle(x, y, radiusNode);
}